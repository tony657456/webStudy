package Chap03;

public class Ex15 {
	public static void main(String[] args) {
		int num = 70;
		
		if (num>=50) {
			System.out.println("50이상의 값이 들어 있습니다");
		}
		else {
			System.out.println("50미만의 값이 들어 있습니다");
		}
		
		int height = 145;
		
		if (height>150) {
			System.out.println("롤러 코스터 타기 가능");
		}
		else {
			System.out.println("롤러 코스터 타기 불가능");
		}
		
	}
}
